function customerSeriesKey(product) {
  const sku = String(product.sku || product.id || "");
  const text = [product.title, product.style, product.collection, product.specs?.construction].join(" ");
  if (/laminate/i.test(text) || sku.startsWith("L24")) return "L240";
  if (sku.startsWith("Y900")) return "Y900";
  if (sku.startsWith("Y800")) return "Y800";
  if (sku.startsWith("WY365") || sku.startsWith("QTY365")) return "QTY365";
  if (sku.startsWith("GE000") || /^E\d+/.test(sku)) return "GE000";
  if (sku.startsWith("WL98")) return "WL980";
  if (sku.startsWith("K57")) return "K570";
  if (sku === "551" || sku === "562") return "560";
  if (sku.startsWith("G")) return "G00";
  return "G00";
}

// Tone descriptor derived from the floor's name/color, used to vary the
// auto-written product copy so each floor reads uniquely (not manufacturer text).
function toneWord(text) {
  const c = String(text || "").toLowerCase();
  if (/white|blonde|pale|light|linen|natural|sand|dune|shell|canvas|limestone|morning/.test(c)) return "light, airy";
  if (/grey|gray|greige|smoke|silver|stone|umber|taupe|ash|fieldstone|slate/.test(c)) return "soft, neutral";
  if (/driftwood|coastal|tide|boardwalk|beige|weathered/.test(c)) return "coastal, sun-washed";
  return "warm, natural";
}

// Unique, locally-relevant description for each floor. Rotates sentence shape by
// position so no two read alike, and always mentions the Bakersfield / Kern
// County service area and carton pricing.
function buildDescription(name, collection, color, index) {
  const tone = toneWord(name + " " + color);
  const variants = [
    `${name} brings a ${tone} wide-plank look to the ${collection}. Built on a 100% waterproof rigid core with a commercial-grade wear layer, it is engineered for everyday Bakersfield living and sold by the full carton with free local delivery across Kern County.`,
    `A ${tone} floor from the ${collection}, ${name} pairs realistic oak grain with a waterproof rigid core that shrugs off spills, pets, and Central Valley dust. Priced honestly by the carton and delivered locally throughout Bakersfield and Kern County.`,
    `${name} is a ${tone} luxury vinyl plank in the ${collection}. Its rigid, 100% waterproof construction and tough wear layer stand up to busy homes and job sites — sold by the carton with free delivery over $1,200 across Kern County.`,
    `Part of the ${collection}, ${name} offers a ${tone} wide plank with a waterproof core and low-maintenance finish. A dependable, good-looking floor for Bakersfield homeowners and contractors, priced by the carton with local delivery.`
  ];
  return variants[index % variants.length];
}

